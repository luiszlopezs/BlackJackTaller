package edu.progavud.taller2pa.vista;

import javax.swing.*;
import java.awt.*;

public class ImagenFondoPanel extends JPanel {
    private Image fondo;

    public ImagenFondoPanel(String rutaImagen) {
        this.fondo = new ImageIcon(getClass().getResource(rutaImagen)).getImage();
    }

    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        if (fondo != null) {
            g.drawImage(fondo, 0, 0, getWidth(), getHeight(), this);
        }
    }
}
